// دالة لتطبيق الترجمات
function applyServicePlannerTranslations() {
    const currentLang = frappe.boot.lang || 'en';
    if (SERVICE_PLANNER_TRANSLATIONS[currentLang]) {
        if (!frappe._messages) {
            frappe._messages = {};
        }
        Object.assign(frappe._messages, SERVICE_PLANNER_TRANSLATIONS[currentLang]);
    }
}
